class FourCal:
    def __init__(self):
        self.first = int(input())
        self.second = int(input())

    def add(self):
        return self.first + self.second

    def sub(self):
        return self.first - self.second

    def mul(self):
        return self.first * self.second

    def div(self):
        return self.first / self.second
